// Variable -------> storage unit

// var, let, const ----------> keyword for declare variable

// var ---------> It help to Redeclare the variable and reassign the value

// Declaration ----------> Give name for the keyword
var fruite

// Assign -------> store value to the variable
fruite = "apple"

// Redeclare
var fruite

// Reassign
fruite = "Orange"
fruite = "Pineapple"

console.log(fruite)


// let ----------> It help to Reassign the value but it cannot redeclare the variable

let car

car = "hundai"
car = "toyoto"

console.log(car)


// const ----------> It cannot be redeclare and reassign

const country = "India"

console.log(country)


// Datatype --------------------------------------------

//Number
const num = 3

//string ---------> sequence of characters enclosed with single or double quotes
const name = "mukil"

//boolean -------> true or false
const bool = true

//undefined -------> If value cannot be stored, it becomes undefined
